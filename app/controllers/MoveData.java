package controllers;

public class MoveData {
    private int initialSquare;
    private int finalSquare;

    public MoveData() {
    }

    public int getInitialSquare() {
        return initialSquare;
    }

    public void setInitialSquare(int initialSquare) {
        this.initialSquare = initialSquare;
    }

    public int getFinalSquare() {
        return finalSquare;
    }

    public void setFinalSquare(int finalSquare) {
        this.finalSquare = finalSquare;
    }
}
